#include <stdio.h>

/*
Given two numeric values, 72 and 104 , 
write a program to print out the corre-
sponding two characters.
*/

int main()
{
    //since this is an int datatype, we output it as a char datatype
    int x = 72, y = 104;

    printf("The character for 72 is => %c\n", x);
    printf("The character for 104 is => %c\n\n", y);
}